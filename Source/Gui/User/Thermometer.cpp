void CWndThermometer::Create(CWnd *pParent, ui16 dwFlags)
{
	CWnd::Create("CWndCharMap", dwFlags, CRect(0, 16, 400, 240), pParent);
}

void DrawTemp(int nTemp)
{
	int nY = 160-(154-34)*nTemp/50;
	CRect rcBar( 50-11-2, 34, 50+13+1, 190-10 );
	for ( int y = rcBar.top; y < rcBar.bottom; y++ )
		for ( int x = rcBar.left; x < rcBar.right; x++ )
		{
			ui16 pix = BIOS::LCD::GetPixel(x, y);
			int nR = Get565R(pix);
			int nG = Get565G(pix);
			int nBlk = max( nR, nG );

			nBlk |= 7;
			if ( y < nY ) 
				pix = CUtils::InterpolateColor( RGB565(000000), RGB565(ffffff), nBlk );
			else
				pix = CUtils::InterpolateColor( RGB565(000000), RGB565(ff0000), nBlk );
			BIOS::LCD::PutPixel(x, y, pix);
		}
}

void CWndThermometer::OnTimer()
{
	static int nTemp = 0;
	nTemp += 1;
	if (nTemp > 50)
		nTemp = 0;
	DrawTemp( nTemp );
//	Invalidate();
}

void CWndThermometer::OnPaint()
{
	BIOS::LCD::Bar( m_rcClient, RGB565(ffffff) );
	CBitmap bmp;
	bmp.Load( bitmapThermometer );
	for ( int i=0; i<16; i++ )
	{
		ui16& clr = bmp.m_arrPalette[i];
		if ( clr == RGB565(ffffff) )
		{
			clr = RGB565(ff00ff);
			continue;
		}
		int r = Get565R( clr );
		//int g = Get565G( clr );
		//int b = Get565B( clr );
		clr = RGB565RGB( r, r, r );
	}

	bmp.Blit ( 20, 21 );

	for ( int x = -24; x <= 24; x++ )
		for ( int y = -24; y <= 24; y++ )
		{
			if ( x*x + y*y > 24*24 )
				continue;
			if ( x*x + y*y < 22*22 )
			{
				BIOS::LCD::PutPixel( 50+x, 206+y, RGB565(ff0000) );	
				continue;
			}
			int l = CUtils::Sqrt( ((x*x + y*y) - 22*22)*25*25 );

			UTILS.Clamp(l, 0, 255);
			ui16 pix = RGB565(ffffff);
			pix = CUtils::InterpolateColor( RGB565(ff0000), pix, l );
			BIOS::LCD::PutPixel( 50+x, 206+y, pix );
		}


	CRect rcBar( 37, 180, 65, 190 );
	for ( int y = rcBar.top; y < rcBar.bottom; y++ )
		for ( int x = rcBar.left; x < rcBar.right; x++ )
		{
			ui16 pix = BIOS::LCD::GetPixel(x, y);
			int nBlk = Get565R(pix);
			nBlk |= 7;
			pix = CUtils::InterpolateColor( RGB565(000000), RGB565(ff0000), nBlk );
			BIOS::LCD::PutPixel(x, y, pix);
		}

	for ( int i=0; i<=50; i+=10 )
		BIOS::LCD::Printf( 70,  154-(154-34)*i/50, RGB565(b0b0b0), RGB565(ffffff), "%2d\xf8" "C", i );
	
	DrawTemp(0);
}

void CWndThermometer::OnKey(ui16 nKey)
{
}

void CWndThermometer::OnMessage(CWnd* pSender, ui16 code, ui32 data)
{
	// LAYOUT ENABLE/DISABLE FROM TOP MENU BAR
	if (code == ToWord('L', 'D') )
	{
		KillTimer();
		return;
	}

	if (code == ToWord('L', 'E') )
	{
		SetTimer(500);
		return;
	}
}


